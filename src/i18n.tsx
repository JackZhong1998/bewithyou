const messages: Record<LanguageCode, Record<string, string>> = {
  'zh-CN': ZH_CN_MESSAGES,
  'zh-TW': ZH_TW_MESSAGES,
  'en-US': EN_US_MESSAGES,
  'ja-JP': JA_JP_MESSAGES,
  'ko-KR': KO_KR_MESSAGES,
  'th-TH': TH_TH_MESSAGES,
  'vi-VN': VI_VN_MESSAGES,
};

const I18nContext = createContext<I18nContextValue | null>(null);

const DEFAULT_LANGUAGE: LanguageCode = 'zh-CN';
const DEFAULT_FALLBACK_LANGUAGE: LanguageCode = 'en-US';

const translate = (language: LanguageCode, key: string, params?: Params): string => {
  const template =
    messages[language]?.[key] ??
    messages[DEFAULT_FALLBACK_LANGUAGE]?.[key] ??
    messages[DEFAULT_LANGUAGE]?.[key] ??
    key;

  if (!params) return template;
  return Object.entries(params).reduce((acc, [k, v]) => {
    return acc.split(`{${k}}`).join(String(v));
  }, template);
};

interface I18nProviderProps {
  language: LanguageCode;
  setLanguage: (language: LanguageCode) => void;
  children: React.ReactNode;
}

export const I18nProvider: React.FC<I18nProviderProps> = ({
  language,
  setLanguage,
  children,
}) => {
  const value = useMemo<I18nContextValue>(() => {
    return {
      language,
      setLanguage,
      t: (key, params) => translate(language, key, params),
    };
  }, [language, setLanguage]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
};

export const useI18n = () => {
  const ctx = useContext(I18nContext);
  if (!ctx) {
    throw new Error('useI18n must be used within I18nProvider');
  }
  return ctx;
};
